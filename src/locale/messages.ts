export const messages: Record<AppLanguage, Dict> = { en, ar }

export function translate(
  lang: AppLanguage,
  key: string,
  vars?: Record<string, string>,
): string {
  let s = messages[lang][key] ?? messages.en[key] ?? key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replaceAll(`{${k}}`, v)
    }
  }
  return s
}
